Ps = {

	dt = 0.1,
	particles = {},
	minlife = 30,
	maxlife = 60,

	new = function(x, y)
		local p = {x=x, y=y, vy=0, c=15, fac=0}
		p.g = -3*random()
		-- p.g = 0
		p.dt = 1/random(Ps.minlife, Ps.maxlife)
		setmetatable(p, Ps.mt)
		table.insert(Ps.particles, p)
		return p
	end,

	mt = {

		__index = {

			update = function(self)
				self.vy = self.vy + self.g * Ps.dt
				self.y = self.y + self.vy * Ps.dt
				self.fac = self.fac + self.dt 
			end,

			draw = function(self)
				pix(self.x, self.y, self.c)
			end,

		}

	},

	emit = function(id, x, y)
		local addr = 0x8000 + id*64
		for i = 0,7 do
			for j = 0,7 do
				if peek4(addr + i*8 + j) > 0 then
					Ps.new(x+j, y+i)
				end
			end
		end
	end,

	update = function(self)
		-- if mouse.md then
