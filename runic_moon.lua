mathFun = {

	between = function(x, a, b)
		return x >= a and x < b
	end,

	clamp = function(x, a, b)
		if mathFun.between(x, a, b) then return x end
		if x < a then return a end
		if x >= b then return b end
	end,

	bool2int = function(a)
		return a and 1 or 0
	end,

	randlist = function(list, n)
		if n ==  nil then n = #list end
		local i = random(1, n)
		return list[i]
	end,

	randfloat = function(a, b)
		return random() * (b - a) + a
	end,

	isin = function(a, list)
		local flag = false
		for i = 1,#list do
			if list[i] == a then 
				flag = true 
				break
			end
		end
		return flag
	end,

}

Vec = {

	new = function(x, y, z)
		local v = {x, y, z}
		setmetatable(v, Vec.mt)
		return v
	end,

	mt = {

		__add = function (u, v)
			return Vec.new(u[1]+v[1], u[2]+v[2], u[3]+v[3])
		end,

		__sub = function(u, v)
			return Vec.new(u[1]-v[1], u[2]-v[2], u[3]-v[3])
		end,

		__mul = function(k, v)
			if type(k) == "table" then
				return Vec.new(k[1]*v[1], k[2]*v[2], k[3]*v[3])
			else
				return Vec.new(k*v[1], k*v[2], k*v[3])
			end
		end,

		__div = function(v, k)
			if type(k) == "table" then
				return Vec.new(v[1]/k[1], v[2]/k[2], v[3]/k[3])
			else
				return Vec.new(v[1]/k, v[2]/k, v[3]/k)
			end
		end,

		__pow = function(v, k)
			return Vec.new(v[1]^k, v[2]^k, v[3]^k)
		end,

		__eq = function(u, v)
			return u[1] == v[1] and u[2] == v[2] and u[3] == v[3]
		end,

		__tostring = function(v)
			return "(" .. v[1] .. "," .. v[2] .. "," .. v[3] .. ")"
		end,

		__concat = function(s, v)
			return s .. "(" .. v[1] .. "," .. v[2] .. "," .. v[3] .. ")"
		end,

		__index = {

			normalise = function(self)
				local r = Vec.norm(self)
				self[1] = self[1] / r
				self[2] = self[2] / r
				self[3] = self[3] / r
				return self
			end,

			rotatex = function(self, ax)
				local c = cos(ax)
				local s = sin(ax)
				self[2], self[3] = self[2]*c-self[3]*s, self[2]*s+self[3]*c
				return self
			end,
		}
	},

	norm = function(v)
		return sqrt(v[1]*v[1] + v[2]*v[2] + v[3]*v[3])
	end,

	dot = function(u, v)
		return u[1]*v[1] + u[2]*v[2] + u[3]*v[3]
	end,

	cross = function(u, v)
		return Vec.new(u[2]*v[3]-v[2]*u[3], v[1]*u[3]-u[1]*v[3], u[1]*v[2]-v[1]*u[2])
	end,

	avg = function(u)
		return (u[1] + u[2] + u[3]) / 3
	end,

	rotatex = function(v, ax)
		local c = cos(ax)
		local s = sin(ax)
		return Vec.new(v[1], v[2]*c-v[3]*s, v[2]*s+v[3]*c)
	end,

}

Bubble = {

	particles = {},
	list = {224, 225, 226, 227, 228, 229},
	pos = {{0,0},{0,1},{1,1},{1,0}},
	r = 7,
	dt = 0.1,
	speed = 6,
	grav = -0.5,
	damping = 1.2,

	over = nil,
	hold = nil,
	dclick = nil,
	dtime = 0,

	new = function(x, y)
		local b = {x=x, y=y}
		b.u = 2*(random()-0.5)
		b.v = -10*random()
		b.id = mathFun.randlist(Bubble.list)
		b.mo = false
		b.t = random(1000)
		table.insert(Bubble.particles, b)
		setmetatable(b, Bubble.mt)
		return b
	end,

	mt = {

		__index = {

			update = function(self)
				-- velocity
				if self.y>60 then
					self.v = self.v + Bubble.grav * Bubble.dt
				end
				-- position
				self.x = self.x + self.u * Bubble.dt
				self.y = self.y + self.v * Bubble.dt
				-- wall collision
		        if self.x < 7 and self.u < 0 then self.u = -self.u/Bubble.damping end
		        if self.x > 233 and self.u > 0 then self.u = -self.u/Bubble.damping end
		        if self.y < 9 and self.v < 0 then self.v = -self.v/Bubble.damping end
		        if self.y > 114 and self.v > 0 then self.v = -self.v/Bubble.damping end
		        -- mouse over
		        self.mo = false
			end,

			draw = function(self)
				local frame = (time.t + self.t) // 200 % 4 + 1
				spr(self.id, self.x-4+Bubble.pos[frame][1], self.y-4+Bubble.pos[frame][2], 0)
				-- spr(self.id, self.x-4, self.y-3, 0)
				spr(256, self.x-7, self.y-7, 0, 1, 0, 0, 2, 2)
				if self.mo == true then
					circb(self.x, self.y, 8, 8)
				end
			end,

			draw_empty = function(self)
				spr(256, self.x-7, self.y-7, 0, 1, 0, 0, 2, 2)
				if self.mo == true then
					circb(self.x, self.y, 8, 8)
				end
			end,

			remove = function(self)
				for i,b in ipairs(Bubble.particles) do
					if b == self then
						table.remove(Bubble.particles, i)
					end
				end
			end

		}

	},

	load = function(self)
		Bubble.particles = {}
		for i = 1,20 do
			local x = random()*240
			local y = random()*107
			Bubble.new(x, y)
		end
	end,

	update = function(self)
		for i,b in ipairs(self.particles) do
			b:update()
		end
		-- drag
		if not mouse.md and not mouse.mu then 
			-- not clicking: update over
			self.hold = nil
			self.over = nil 
			for i,b in ipairs(self.particles) do
				local r = sqrt((b.x - mouse.x)^2 + (b.y - mouse.y)^2)
				if r < 8 then
					b.mo = true
					self.over = b
					break
				end
			end
		elseif mouse.mp then
			-- on click: hold <- over
			self.hold = self.over 
			if self.dclick == self.over and self.dclick ~= nil then
				self.dclick:remove()
				sfx(49)
				if scene.current == scene.game then
					Ps.emit(self.dclick.id, self.dclick.x-4, self.dclick.y-3)
				end
			else
				self.dclick = self.over
				self.dtime = 0
			end
		end
		if mouse.md and self.hold ~= nil then
			-- is holding: move object
			self.hold.mo = true
			self.hold.x, self.hold.u = mouse.x, (mouse.x-self.hold.x) 
			self.hold.y, self.hold.v = mouse.y, (mouse.y-self.hold.y) 
		elseif mouse.mu and self.hold ~= nil then
			-- on release: check cauldron 
			if pot.mo and pot.id == self.hold.id then
				player.health = mathFun.clamp(player.health+10, 0, 100)
				pot.id = mathFun.randlist(Bubble.list)
				sfx(48)
				self.hold:remove()
				Ps.emit(self.hold.id, 113, 88+time.ts%2)
				level.score = level.score + 1
			end
			
		end
		self.dtime = self.dtime + 1
		if self.dtime > 60 then
			self.dclick = nil
		end

		-- collision
		for i = 1,#self.particles do 
	        for j = (i+1),#self.particles do
	            Bubble.collision(self.particles[i], self.particles[j])
	        end
	    end
	end,

	draw = function(self)
		for i,b in ipairs(self.particles) do
			b:draw()
		end
	end,

	draw_empty = function(self)
		for i,b in ipairs(self.particles) do
			b:draw_empty()
		end
	end,

	collision = function(b1, b2)
		local x = b1.x - b2.x
	    local y = b1.y - b2.y
	    local r = sqrt(x*x + y*y)
	    if r  > 14 then
	        return
	    end
	    local nx = x / r
	    local ny = y / r
	    local per1 = b1.u * nx + b1.v * ny
	    local per2 = b2.u * nx + b2.v * ny
	    if per1 > 0 and per2 < 0 then
	        return
	    end
	    local tan1 = b1.u * ny - b1.v * nx
	    local tan2 = b2.u * ny - b2.v * nx
	    b1.u = per2 * nx + tan1 * ny
	    b1.v = per2 * ny - tan1 * nx
	    b2.u = per1 * nx + tan2 * ny
	    b2.v = per1 * ny - tan2 * nx
	end,

}

